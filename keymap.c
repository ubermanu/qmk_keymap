bool process_record_user(uint16_t keycode, keyrecord_t *record) {

  // Release the GUI key if it was pressed before sending the keypress
  if (record->event.pressed && keyboard_report->mods & MOD_BIT(KC_LGUI)) {
    switch (keycode) {
      case OS_CUT: // Cut (Shift+Delete)
        unregister_mods(MOD_BIT(KC_LGUI));
        register_code(KC_LSFT);
        register_code(KC_DEL);
        unregister_code(KC_DEL);
        unregister_code(KC_LSFT);
        register_mods(MOD_BIT(KC_LGUI));
        return false;
        break;
      case OS_COPY: // Copy (Ctrl+Insert)
        unregister_mods(MOD_BIT(KC_LGUI));
        register_code(KC_LCTL);
        register_code(KC_INS);
        unregister_code(KC_INS);
        unregister_code(KC_LCTL);
        register_mods(MOD_BIT(KC_LGUI));
        return false;
        break;
      case OS_PASTE: // Paste (Shift+Insert)
        unregister_mods(MOD_BIT(KC_LGUI));
        register_code(KC_LSFT);
        register_code(KC_INS);
        unregister_code(KC_INS);
        unregister_code(KC_LSFT);
        register_mods(MOD_BIT(KC_LGUI));
        return false;
        break;
      case OS_UNDO: // Undo (Ctrl+Z)
        unregister_mods(MOD_BIT(KC_LGUI));
        register_code(KC_LCTL);
        register_code(DV_Z);
        unregister_code(DV_Z);
        unregister_code(KC_LCTL);
        register_mods(MOD_BIT(KC_LGUI));
        return false;
        break;
      case OS_REDO: // Redo (Shift+Ctrl+Z)
        unregister_mods(MOD_BIT(KC_LGUI));
        register_code(KC_LCTL);
        register_code(KC_LSFT);
        register_code(DV_Z);
        unregister_code(DV_Z);
        unregister_code(KC_LSFT);
        unregister_code(KC_LCTL);
        register_mods(MOD_BIT(KC_LGUI));
        return false;
        break;
    }
  }

  switch (keycode) {
    case BASE:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_BASE);
      }
      return false;
      break;
    case LOWER:
      if (record->event.pressed) {
        layer_on(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case RAISE:
      if (record->event.pressed) {
        layer_on(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case ADJUST:
      if (record->event.pressed) {
        layer_on(_ADJUST);
      } else {
        layer_off(_ADJUST);
      }
      return false;
      break;
  }

  // Soft accent layers
  switch (keycode) {
    case DV_A:
      if (record->event.pressed) {
        layer_on(_A_ACCENTS);
      } else {
        layer_off(_A_ACCENTS);
      }
      break;
    case DV_E:
      if (record->event.pressed) {
        layer_on(_E_ACCENTS);
      } else {
        layer_off(_E_ACCENTS);
      }
      break;
    case DV_I:
      if (record->event.pressed) {
        layer_on(_I_ACCENTS);
      } else {
        layer_off(_I_ACCENTS);
      }
      break;
    case DV_O:
      if (record->event.pressed) {
        layer_on(_O_ACCENTS);
      } else {
        layer_off(_O_ACCENTS);
      }
      break;
    case DV_U:
      if (record->event.pressed) {
        layer_on(_U_ACCENTS);
      } else {
        layer_off(_U_ACCENTS);
      }
      break;
  }

  return true;
}
